export function getLang() {
  return localStorage.getItem("shift_cms_lang") || "cs";
}

export function setLang(lang) {
  localStorage.setItem("shift_cms_lang", lang);
  window.dispatchEvent(new Event("lang-change"));
}

export function t(key) {
  const lang = getLang();
  return translations[lang]?.[key] ?? translations["cs"][key] ?? key;
}

export function useI18n() {
  const [lang, setLangState] = React.useState(getLang);

  React.useEffect(() => {
    function onLangChange() { setLangState(getLang()); }
    window.addEventListener("lang-change", onLangChange);
    return () => window.removeEventListener("lang-change", onLangChange);
  }, []);

  return {
    lang,
    setLang: (l) => { setLang(l); setLangState(l); },
    t: (key) => translations[lang]?.[key] ?? translations["cs"][key] ?? key,
    tf: (key, ...args) => {
      const fn = translations[lang]?.[key] ?? translations["cs"][key];
      return typeof fn === "function" ? fn(...args) : fn;
    },
  };
}